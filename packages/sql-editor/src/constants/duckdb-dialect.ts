// SQL language configuration for Monaco Editor
export const SQL_LANGUAGE_CONFIGURATION: Monaco.languages.IMonarchLanguage = {
  defaultToken: '',
  tokenPostfix: '.sql',
  ignoreCase: true,

  brackets: [
    {open: '[', close: ']', token: 'delimiter.square'},
    {open: '(', close: ')', token: 'delimiter.parenthesis'},
  ],

  keywords: DUCKDB_KEYWORDS,
  operators: SQL_OPERATORS,
  builtinFunctions: DUCKDB_FUNCTIONS,
  builtinVariables: SQL_VARIABLES,
  pseudoColumns: SQL_PSEUDO_COLUMNS,

  tokenizer: {
    root: [
      {include: '@comments'},
      {include: '@whitespace'},
      {include: '@numbers'},
      {include: '@strings'},
      {include: '@complexIdentifiers'},
      {include: '@scopes'},
      [/[;,.]/, 'delimiter'],
      [/[()]/, '@brackets'],
      [
        /[\w@#$]+/,
        {
          cases: {
            '@keywords': 'keyword',
            '@operators': 'operator',
            '@builtinFunctions': 'predefined',
            '@builtinVariables': 'predefined',
            '@pseudoColumns': 'predefined',
            '@default': 'identifier',
          },
        },
      ],
      [/[<>=!%&+\-*/|~^]/, 'operator'],
    ],
    whitespace: [[/\s+/, 'white']],
    comments: [
      [/--+.*/, 'comment'],
      [/\/\*/, {token: 'comment.quote', next: '@comment'}],
    ],
    comment: [
      [/[^*/]+/, 'comment'],
      [/\*\//, {token: 'comment.quote', next: '@pop'}],
      [/./, 'comment'],
    ],
    numbers: [
      [/0[xX][0-9a-fA-F]*/, 'number'],
      [/[$][+-]*\d*(\.\d*)?/, 'number'],
      [/((\d+(\.\d*)?)|(\.\d+))([eE][\-+]?\d+)?/, 'number'],
    ],
    strings: [
      [/'/, {token: 'string', next: '@string'}],
      [/"/, {token: 'string.double', next: '@stringDouble'}],
    ],
    string: [
      [/[^']+/, 'string'],
      [/''/, 'string'],
      [/'/, {token: 'string', next: '@pop'}],
    ],
    stringDouble: [
      [/[^"]+/, 'string.double'],
      [/""/, 'string.double'],
      [/"/, {token: 'string.double', next: '@pop'}],
    ],
    complexIdentifiers: [
      [/\[/, {token: 'identifier.quote', next: '@bracketedIdentifier'}],
      [/"/, {token: 'identifier.quote', next: '@quotedIdentifier'}],
    ],
    bracketedIdentifier: [
      [/[^\]]+/, 'identifier'],
      [/]]/, 'identifier'],
      [/]/, {token: 'identifier.quote', next: '@pop'}],
    ],
    quotedIdentifier: [
      [/[^"]+/, 'identifier'],
      [/""/, 'identifier'],
      [/"/, {token: 'identifier.quote', next: '@pop'}],
    ],
    scopes: [],
  },
} as const satisfies Monaco.languages.IMonarchLanguage;
